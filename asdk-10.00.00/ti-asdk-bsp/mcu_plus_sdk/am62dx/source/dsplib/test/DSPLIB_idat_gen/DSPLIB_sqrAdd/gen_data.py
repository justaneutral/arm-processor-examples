def gen_testCaseParams(testParamsFile, currPrm):
    """Generate and write test case parameters to idat file

    Args:
        param1 (file object): Input file object to idat.c file
        param2 (dict object): Dict object with test case parameter

    """

    testId = currPrm["ID"]
    dTypeIn0 = currPrm["dTypeIn0"]
    length = currPrm["length"]
    testType = currPrm["testType"]
    dType = currPrm["DSP_dTypeIn"]
    dTypeOut = currPrm["DSP_dTypeOut"]
    demoCase = currPrm["demoCase"]
    numReps = currPrm["numReps"]
    outputDataLocation = currPrm["outputDataLocation"]

    if testType == "STATIC":
        in0 = "staticRefIn0Case" + testId
        out = "staticRefOutCase" + testId
    else:
        in0 = "NULL"
        out = "NULL"

    if demoCase == "true":
        testParamsFile.write(
            "#if (defined(ALL_TEST_CASES) || (TEST_CASE == %d) || defined(DEMO_CASE))\n"
            % (int(testId))
        )
    else:
        testParamsFile.write(
            "#if (defined(ALL_TEST_CASES) || (TEST_CASE == %d))\n" % (int(testId))
        )

    idatFile.write("{\n")
    idatFile.write("%s, // Test pattern \n" % ((testType)))
    idatFile.write("%s, // Input vector 0 \n" % ((in0)))
    idatFile.write("%s, // Output vector \n" % ((out)))
    idatFile.write("%s, // Input data type \n" % ((dType)))
    idatFile.write("%s, // Input data length \n" % ((length)))
    idatFile.write("%s, // Output data type \n" % ((dTypeOut)))
    idatFile.write("%s, // Output data length \n" % ((1)))
    idatFile.write("%s, // Output data location \n" % (outputDataLocation))
    idatFile.write("%d, // number of reps. \n" % (int(numReps)))
    idatFile.write("%d, // test ID\n" % int(testId))
    idatFile.write("},\n")
    idatFile.write("#endif")
    idatFile.write("\n\n")


def gen_idat_file(idatFile, testCases, testIdList):
    """Generate the idat file

    Args:
        param1 (file object): Input file object to idat.c file
        param2 (dict object): Dict object with test case parameter

    """

    fIn = open("../common/ti_lice_header_idat.txt", "r")
    lines = fIn.readlines()
    fIn.close()
    idatFile.writelines(lines)
    includeString = '#include "DSPLIB_sqrAdd_idat.h"'
    idatFile.writelines(includeString)
    idatFile.write("\n")
    idatFile.write("/*This file is autogenerated*/\n")
    idatFile.write("/*Please do not edit*/\n")
    idatFile.write("\n\n")
    # generate ifdefs for header files
    for testCase in testCases:
        if int(testCase["ID"]) in testIdList:
            if testCase["testType"] == "STATIC":
                DSPLIB_utils.gen_ifdefs(idatFile, testCase)

    # generate parameters for test cases
    idatFile.write("static DSPLIB_sqrAdd_testParams_t testParams[] =  ")
    idatFile.write("{\n")

    for testCase in testCases:
        if int(testCase["ID"]) in testIdList:
            gen_testCaseParams(idatFile, testCase)

    idatFile.write("};\n\n")
    fIn = open("test_param_function.txt", "r")
    lines = fIn.readlines()

    idatFile.writelines(lines)


def process_test_case(testCase):
    """Convert str to int for necessary parameters

    Args:
        Param1(:obj: dict) : Dictionary object with test case parameters

    Returns:
        Obj: Dictionary object with necessary parameters converted to int

    """

    # print(testCase.keys())
    testCase["testId"] = int(testCase["ID"])
    testCase["length"] = int(testCase["length"])

    return testCase


def gen_test_case_header_file(testCase):
    """Generate test case and write out input, filter, and output tensor
    into a header file usable in C/C++ code.

    Args:
        param1 (:obj: dict): A dict object with all the test case
        parameters

    Returns:
        [int int]: Minimum and maximum value in output feature tensor

    """

    ##############################
    # Get test parameter of case #
    ##############################

    testId = testCase["ID"]
    length = testCase["length"]
    dTypeIn0 = testCase["dTypeIn0"]
    dTypeOut = testCase["dTypeOut"]

    if dTypeIn0 == "int8_t" or dTypeIn0 == "uint8_t":
        bits = 8
    elif dTypeIn0 == "int16_t" or dTypeIn0 == "uint16_t":
        bits = 16
    elif dTypeIn0 == "int32_t" or dTypeIn0 == "uint32_t":
        bits = 16  # Reduced bits from  32 to avoid overflow
    elif dTypeIn0 == "float":
        bits = 32
    else:
        bits = 64

    print("bits =", bits)
    # unsigned integer
    if dTypeIn0[0] == "u":
        minVal = 0
        maxVal = (2 ** bits) - 1
    else:
        minVal = -(2 ** (bits - 1))
        maxVal = 2 ** (bits - 1)
    print("min = :", minVal, " max = :", maxVal)
    if dTypeIn0 == "int8_t":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype("int8")
    elif dTypeIn0 == "int16_t":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype("int16")
    elif dTypeIn0 == "int32_t":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype("int32")
    elif dTypeIn0 == "int64_t":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype("int64")
    elif dTypeIn0 == "uint8_t":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype("uint8")
    elif dTypeIn0 == "uint16_t":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype(
            "uint16"
        )
    elif dTypeIn0 == "uint32_t":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype(
            "uint32"
        )
    elif dTypeIn0 == "uint64_t":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype(
            "uint64"
        )
    elif dTypeIn0 == "float":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype("float")
    elif dTypeIn0 == "double":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(length,)).astype(
            "double"
        )

    # generate output
    # The input two input vectors should be casted in order to get the correct out from the multiplication process.
    if dTypeOut == "int16_t":
        out = sum((in0.astype("int16") * in0.astype("int16")))
    elif dTypeOut == "int32_t":
        out = sum((in0.astype("int32") * in0.astype("int32")))
    elif dTypeOut == "int64_t":
        out = sum((in0.astype("int64") * in0.astype("int64")))
    elif dTypeOut == "uint16_t":
        out = sum((in0.astype("uint16") * in0.astype("uint16")))
    elif dTypeOut == "uint32_t":
        out = sum((in0.astype("uint32") * in0.astype("uint32")))
    elif dTypeOut == "uint64_t":
        # if dTypeIn0 == "uint16_t":
        out = sum(in0.astype("uint32") * in0.astype("uint32"))
        # else:
        # out = sum(in0* in1).astype("uint64")
    elif dTypeOut == "float":
        out = sum((in0.astype("float") * in0.astype("float")))
    elif dTypeOut == "double":
        out = sum((in0.astype("double") * in0.astype("double")))

    headerFileName = "staticRefCase" + str(testId) + ".h"
    file_io.write_header_file(in0, out, testCase, headerFileName)

    return [np.min(out), np.max(out)]


# CSV file with paratmeters for all test cases
testCasesCsvFile = "test_cases_list.csv"
testIdList = []  # list of test IDs that we want to generate test data
testIdList += range(1, 65)

myArgs = DSPLIB_utils.getCmdLineArgs()


def gen_test_case(testCase):
    if testCase["testType"] == "STATIC":
        [minY, maxY] = gen_test_case_header_file(testCase)
        print(
            "Test case generation completed for test ID",
            testCase["ID"],
            "w/ min:",
            minY,
            "and max",
            maxY,
        )
    else:
        print(
            "Skipping test case generation for test ID",
            testCase["ID"],
            "as tesType is RANDOM",
        )


#############################################################################
# Open CSV file with test cases and generate header file with test data for #
# test cases of interest; based on testIdList                               #
#############################################################################

with open(testCasesCsvFile, encoding="utf-8-sig") as csv_file:
    testCaseReader = csv.DictReader(csv_file)
    testCases = list(testCaseReader)

    for testCase in testCases:
        testCase = process_test_case(testCase)

    for testCase in testCases:
        if myArgs.allCases == True:
            gen_test_case(testCase)
        elif int(testCase["ID"]) in testIdList:
            gen_test_case(testCase)

idatFileName = "../../DSPLIB_sqrAdd/DSPLIB_sqrAdd_idat.c"


##########################################################################
# Generate idat.cpp file by parsing all the test cases in the CSV file # #
##########################################################################


with open(idatFileName, "w") as idatFile:
    gen_idat_file(idatFile, testCases, testIdList)


if platform == "linux" or platform == "linux2":
    call(["clang-format", "-style=file", "-i", idatFileName])
    call(["indent", "-nut", "-i3", "-c55", "-l200", idatFileName])
    call(["rm", idatFileName + "~"])  # remove backup file


print("Idat file generation completed")
