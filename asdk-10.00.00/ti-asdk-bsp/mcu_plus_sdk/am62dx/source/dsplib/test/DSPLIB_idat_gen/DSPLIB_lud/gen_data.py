def lu_decomposition(A):
    n = len(A)
    P = np.eye(n)
    L = np.eye(n)
    U = np.copy(A)

    for k in range(n-1):
        # Pivoting: Find the row with the maximum value in the current column
        max_row = np.argmax(np.abs(U[k:, k])) + k
        min_row = np.argmin(np.abs(U[k:, k])) + k

        if max_row != k:
            # Swap rows in U
            U[[min_row, max_row], :] = U[[max_row, min_row], :]
            # Swap rows in P
            P[[min_row, max_row], :] = P[[max_row, min_row], :]
            # Swap rows in L (up to column k)
        for i in range(k+1, n):
            U[i, k] /= U[k, k]
            U[i, k+1:] -=  U[i, k] * U[k, k+1:]
    for k in range(n):
        L[k, :k] = U[k, :k]
        U[k, :k] = 0
    #print(P@L@U)
    return P, L, U

def gen_testCaseParams(testParamsFile, currPrm):
    """Generate and write test case parameters to idat file

    Args:
        param1 (file object): Input file object to idat.c file
        param2 (dict object): Dict object with test case parameter

    """

    testId = currPrm["ID"]
    dType = currPrm["dType"]
    order = currPrm["order"]
    strideOrder = currPrm["strideOrder"]
    strideP = currPrm["strideP"]
    testType = currPrm["testType"]
    demoCase = currPrm["demoCase"]
    outputDataLocation = currPrm["outputDataLocation"]
    numReps = currPrm["numReps"]

    if testType == "STATIC":
        in0 = "staticRefInACase" + testId
        in1 = "staticRefInBCase" + testId
        out0 = "staticRefOutPCase" + testId
        out1 = "staticRefOutLCase" + testId
        out2 = "staticRefOutUCase" + testId
        out3 = "staticRefOutXCase" + testId
        out4 = "staticRefOutInvACase" + testId
    else:
        in0 =  "NULL"
        in1 =  "NULL"
        out0 = "NULL"
        out1 = "NULL"
        out2 = "NULL"
        out3 = "NULL"
        out4 = "NULL"
      

    if demoCase == "true":
        testParamsFile.write(
            "#if (defined(ALL_TEST_CASES) || (TEST_CASE == %d) || defined(DEMO_CASE))\n"
            % (int(testId))
        )
    else:
        testParamsFile.write(
            "#if (defined(ALL_TEST_CASES) || (TEST_CASE == %d))\n" % (int(testId))
        )

    idatFile.write("{\n")
    idatFile.write("%s, // Test pattern \n" % ((testType)))
    idatFile.write("%s, // Input matrix A \n" % ((in0)))
    idatFile.write("%s, // Input array B \n" % ((in1)))
    idatFile.write("%s, // Output matrix P \n" % ((out0)))
    idatFile.write("%s, // Output matrix L \n" % ((out1)))
    idatFile.write("%s, // Output matrix U \n" % ((out2)))
    idatFile.write("%s, // Output matrix X \n" % ((out3)))
    idatFile.write("%s, // Output matrix invA \n" % ((out4)))
    idatFile.write("%s, // dataType \n" % ((DSPLIB_utils.resolve_dType(dType))))
    idatFile.write("%s, // order \n" % ((order)))
    idatFile.write("DSPLIB_CALC_STRIDE(%s * sizeof(%s), DSPLIB_ALIGN_SHIFT_64BYTES), // stride of A matrix \n" % ((strideOrder), (dType)))
    idatFile.write("DSPLIB_CALC_STRIDE(%s * sizeof(unsigned short), DSPLIB_ALIGN_SHIFT_64BYTES), // stride of P matrix \n" % ((strideP)))
    idatFile.write("%s, // shift \n" % ((0)))    
    idatFile.write("%s, // Output data location \n" % (outputDataLocation))
    idatFile.write("%d, // number of reps. \n" % (int(numReps)))
    idatFile.write("%d, // test ID\n" % int(testId))
    idatFile.write("},\n")
    idatFile.write("#endif\n")

    idatFile.write("\n\n")


def gen_idat_file(idatFile, testCases):
    """Generate the idat file

    Args:
        param1 (file object): Input file object to idat.c file
        param2 (dict object): Dict object with test case parameter

    """

    fIn = open("../common/ti_lice_header_idat.txt", "r")
    lines = fIn.readlines()
    fIn.close()
    idatFile.writelines(lines)
    includeString = '#include "DSPLIB_lud_idat.h"'
    idatFile.writelines(includeString)
    idatFile.write("\n")
    idatFile.write("/*This file is autogenerated*/\n")
    idatFile.write("/*Please do not edit*/\n")
    idatFile.write("\n\n")
    # generate ifdefs for header files
    for testCase in testCases:
        if testCase["testType"] == "STATIC":
            DSPLIB_utils.gen_ifdefs(idatFile, testCase)

    # generate parameters for test cases
    idatFile.write("static DSPLIB_lud_testParams_t testParams[] =  ")
    idatFile.write("{\n")

    for testCase in testCases:
        gen_testCaseParams(idatFile, testCase)

    idatFile.write("};\n\n")
    fIn = open("test_param_function.txt", "r")
    lines = fIn.readlines()

    idatFile.writelines(lines)


