def gen_test_case_header_file(testCase):
    """Generate test case and write out input, filter, and output tensor
    into a header file usable in C/C++ code.

    Args:
        param1 (:obj: dict): A dict object with all the test case
        parameters

    Returns:
        [int int]: Minimum and maximum value in output feature tensor

    """

    ##############################
    # Get test parameter of case #
    ##############################

    testId = testCase["ID"]
    dType = testCase["dType"]
    order = testCase["order"]
    [minVal, maxVal] = [-10, 10]

    if dType[0] == "f":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(order, order)).astype("float32")
        np.round(in0, decimals=2, out=in0)
        in1 = np.random.uniform(low=minVal, high=maxVal, size=(order)).astype("float32")
        np.round(in1, decimals=2, out=in1)
        #out0, out1, out2 = sp.linalg.lu(in0)
        P,L,U=lu_decomposition(in0)
        out3 = sp.linalg.solve((P.T @ L @ U), in1)
        out4 = sp.linalg.inv((P.T @ L @ U))

    elif dType[0] == "d":
        in0 = np.random.uniform(low=minVal, high=maxVal, size=(order, order)).astype("double")
        in1 = np.random.uniform(low=minVal, high=maxVal, size=(order)).astype("double")
        #out0, out1, out2 = sp.linalg.lu(in0)
        P,L,U=lu_decomposition(in0)
        out3 = sp.linalg.solve((P.T @ L @ U), in1)
        out4 = sp.linalg.inv((P.T @ L @ U))

    headerFileName = "staticRefCase" + str(testId) + ".h"
    file_io.write_header_file(in0, in1,P, L, U, out3,out4,testCase, headerFileName)

    return [np.min([np.min(L),np.min(U)]), np.max(U)]


