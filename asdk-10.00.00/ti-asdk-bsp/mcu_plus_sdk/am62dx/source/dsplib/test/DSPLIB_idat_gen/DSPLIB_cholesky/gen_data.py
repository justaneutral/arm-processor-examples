def generate_symmetric_matrix(random_matrix):

    # Make it symmetric by adding it to its transpose
    symmetric_matrix = random_matrix + random_matrix.T

    return symmetric_matrix


def gen_testCaseParams(testParamsFile, currPrm):
    """Generate and write test case parameters to idat file

    Args:
        param1 (file object): Input file object to idat.c file
        param2 (dict object): Dict object with test case parameter

    """

    testId = currPrm["ID"]
    dType = currPrm["dType"]
    order = currPrm["order"]
    strideIn = currPrm["strideIn"]
    strideOut = currPrm["strideOut"]
    enableTest = currPrm["enableTest"]
    # print(currPrm)

    testPatternA = currPrm["testPatternA"]
    testPatternB = currPrm["testPatternB"]
    demoCase = currPrm["demoCase"]
    outputDataLocation = currPrm["outputDataLocation"]
    numReps = currPrm["numReps"]

    if testPatternA == "STATIC":
        in0 = "staticRefInCase" + testId
        in1 = "staticRefInBCase" + testId
        out0 = "staticRefOutCase" + testId
        out1 = "staticRefOutXCase" + testId

    else:
        in0 = "NULL"
        in1 = "NULL"
        out0 = "NULL"
        out1 = "NULL"

    if demoCase == "true":
        testParamsFile.write(
            "#if (defined(ALL_TEST_CASES) || (TEST_CASE == %d) || defined(DEMO_CASE))\n"
            % (int(testId))
        )
    else:
        testParamsFile.write(
            "#if (defined(ALL_TEST_CASES) || (TEST_CASE == %d))\n" % (int(testId))
        )

    idatFile.write("{\n")
    idatFile.write("%s, // testPatternA \n" % ((testPatternA)))
    idatFile.write("%s, // testPatternB \n" % ((testPatternB)))
    idatFile.write("%s, // Input matrix  \n" % ((in0)))
    idatFile.write("%s, // Input array B \n" % ((in1)))
    idatFile.write("%s, // Output matrix Upper Triangle Mat \n" % ((out0)))
    idatFile.write("%s, // Output matrix X \n" % ((out1)))
    idatFile.write("%s, // dataType \n" % ((DSPLIB_utils.resolve_dType(dType))))
    idatFile.write("%s, // order \n" % ((order)))
    idatFile.write("%s, // enableTest \n" % ((enableTest)))
    idatFile.write(
        "DSPLIB_CALC_STRIDE(%s * sizeof(%s), DSPLIB_ALIGN_SHIFT_64BYTES), // stride of Inp matrix \n"
        % ((strideIn), (dType))
    )
    idatFile.write(
        "DSPLIB_CALC_STRIDE(%s * sizeof(%s), DSPLIB_ALIGN_SHIFT_64BYTES), // stride of Out matrix \n"
        % ((strideIn), (dType))
    )
    idatFile.write("%s, // shift \n" % ((0)))
    idatFile.write("%s, // Output data location \n" % (outputDataLocation))
    idatFile.write("%d, // number of reps. \n" % (int(numReps)))
    idatFile.write("%d, // test ID\n" % int(testId))
    idatFile.write("},\n")
    idatFile.write("#endif\n")

    idatFile.write("\n\n")


def gen_idat_file(idatFile, testCases):
    """Generate the idat file

    Args:
        param1 (file object): Input file object to idat.c file
        param2 (dict object): Dict object with test case parameter

    """

    fIn = open("../common/ti_lice_header_idat.txt", "r")
    lines = fIn.readlines()
    fIn.close()
    idatFile.writelines(lines)
    includeString = '#include "DSPLIB_cholesky_idat.h"'
    idatFile.writelines(includeString)
    idatFile.write("\n")
    idatFile.write("/*This file is autogenerated*/\n")
    idatFile.write("/*Please do not edit*/\n")
    idatFile.write("\n\n")
    # generate ifdefs for header files
    for testCase in testCases:
        if testCase["testPatternA"] == "STATIC":
            DSPLIB_utils.gen_ifdefs(idatFile, testCase)

    # generate parameters for test cases
    idatFile.write("static DSPLIB_cholesky_testParams_t testParams[] =  ")
    idatFile.write("{\n")

    for testCase in testCases:
        gen_testCaseParams(idatFile, testCase)

    idatFile.write("};\n\n")
    fIn = open("test_param_function.txt", "r")
    lines = fIn.readlines()

    idatFile.writelines(lines)


def process_test_case(testCase):
    """Convert str to int for necessary parameters

    Args:
        Param1(:obj: dict) : Dictionary object with test case parameters

    Returns:
        Obj: Dictionary object with necessary parameters converted to int

    """

    # print(testCase.keys())
    testCase["testId"] = int(testCase["ID"])
    testCase["order"] = int(testCase["order"])
    testCase["strideIn"] = int(testCase["strideIn"])
    testCase["strideOut"] = int(testCase["strideOut"])
    testCase["enableTest"] = int(testCase["enableTest"])

    # testCase["enableReducedForm"] = int(testCase["enableReducedForm"])

    return testCase


def gen_test_case_header_file(testCase):
    """Generate test case and write out input, filter, and output tensor
    into a header file usable in C/C++ code.

    Args:
        param1 (:obj: dict): A dict object with all the test case
        parameters

    Returns:
        [int int]: Minimum and maximum value in output feature tensor

    """

    ##############################
    # Get test parameter of case #
    ##############################

    testId = testCase["ID"]
    dType = testCase["dType"]
    order = testCase["order"]

    if dType[0] == "f":
        in0 = make_spd_matrix(n_dim=order, random_state=0)
        in1 = np.full(order, order).astype("float32")
        try:
            out0 = sp.linalg.cholesky(in0)
        except np.linalg.LinAlgError:
            print("Matrix is not positive definite.")
        out1 = sp.linalg.cho_solve((out0, False), in1)

    elif dType[0] == "d":
        in0 = make_spd_matrix(n_dim=order, random_state=0)
        in1 = np.full(order, order).astype("float32")
        try:
            out0 = sp.linalg.cholesky(in0)
        except np.linalg.LinAlgError:
            print("Matrix is not positive definite.")
        out1 = sp.linalg.cho_solve((out0, False), in1)

    headerFileName = "staticRefCase" + str(testId) + ".h"
    file_io.write_header_file(in0, in1, out0, out1, testCase, headerFileName)

    return [np.min(out0), np.max(out0)]


