def gen_testCaseParams(testParamsFile, currPrm):
    """Generate and write test case parameters to idat file

    Args:
        param1 (file object): Input file object to idat.c file
        param2 (dict object): Dict object with test case parameter

    """

    testId = currPrm["ID"]
    dTypeIn = currPrm["dTypeIn"]
    dTypeOut = currPrm["dTypeOut"]
    width = currPrm["width"]
    height = currPrm["height"]
    strideIn = currPrm["strideIn"]
    testType = currPrm["testType"]
    demoCase = currPrm["demoCase"]
    numReps = currPrm["numReps"]
    outputDataLocation = currPrm["outputDataLocation"]
    pixelsProcessed = currPrm["pixelsProcessed"]
    currentSum = currPrm["currentSum"]
    currentSqSum = currPrm["currentSqSum"]
    
    # hard code block-based computation parameters since constant
    dTypePixelsProcessed = "uint32_t"
    
    if dTypeIn == "uint8_t":
        dTypeCurrentSum = "uint32_t"
        dTypeCurrentSqSum = "uint32_t"
    else:
        dTypeCurrentSum = "uint64_t"
        dTypeCurrentSqSum = "uint64_t"
   
    if testType == "STATIC":
        in0 = "staticRefInCase" + testId
        pixelsProcessedIn = "staticRefPixelsProcessedInCase" + testId
        currentSumIn = "staticRefCurrentSumInCase" + testId
        currentSqSumIn = "staticRefCurrentSqSumInCase" + testId
        out0 = "staticRefOut0Case" + testId
        out1 = "staticRefOut1Case" + testId
        pixelsProcessedOut = "staticRefPixelsProcessedOutCase" + testId
        currentSumOut = "staticRefCurrentSumOutCase" + testId
        currentSqSumOut = "staticRefCurrentSqSumOutCase" + testId

    else:
        in0 = "NULL"
        pixelsProcessedIn = "NULL"
        currentSumIn = "NULL"
        currentSqSumIn = "NULL"
        pixelsProcessedOut = "NULL"
        currentSumOut = "NULL"
        currentSqSumOut = "NULL"

        out0 = "NULL"
        out1 = "NULL"

    if demoCase == "true":
        testParamsFile.write(
            "#if (defined(ALL_TEST_CASES) || (TEST_CASE == %d) || defined(DEMO_CASE))\n"
            % (int(testId))
        )
    else:
        testParamsFile.write(
            "#if (defined(ALL_TEST_CASES) || (TEST_CASE == %d))\n" % (int(testId))
        )

    idatFile.write("{\n")
    idatFile.write("%s, // Test pattern \n" % ((testType)))
    idatFile.write("%s, // Input vector \n" % ((in0)))
    idatFile.write(
        "%s, // data type in \n" % (VXLIB_utils.resolveVXLIBDType(dTypeIn))
    )
    idatFile.write("%s, // Mean Output vector \n" % ((out0)))
    idatFile.write("%s, // Standard Deviation Output vector \n" % ((out1)))
    idatFile.write(
        "%s, // data type out \n" % (VXLIB_utils.resolveVXLIBDType(dTypeOut))
    )
    idatFile.write("%d, // width of image \n" % ((width)))
    idatFile.write("%d, // height of image \n" % ((height)))
    idatFile.write("%d* sizeof(%s), // strideIn \n" % ((strideIn), (dTypeIn)))
    idatFile.write("%s, // number of pixels processed in \n" % ((pixelsProcessedIn)))
    idatFile.write("%s, // current sum in \n" % ((currentSumIn)))
    idatFile.write("%s, // current square sum in \n" % ((currentSqSumIn)))
    idatFile.write("%s, // number of pixels processed out \n" % ((pixelsProcessedOut)))
    idatFile.write("%s, // current sum out \n" % ((currentSumOut)))
    idatFile.write("%s, // current square sum out \n" % ((currentSqSumOut)))
    idatFile.write("%s, // Output data location \n" % (outputDataLocation))
    idatFile.write("%d, // number of reps. \n" % (int(numReps)))
    idatFile.write("%d, // test ID\n" % int(testId))
    idatFile.write("},\n")
    idatFile.write("#endif")
    idatFile.write("\n\n")


def gen_idat_file(idatFile, testCases):
    """Generate the idat file

    Args:
        param1 (file object): Input file object to idat.c file
        param2 (dict object): Dict object with test case parameter

    """

    fIn = open("../common/ti_lice_header_idat.txt", "r")
    lines = fIn.readlines()
    fIn.close()
    idatFile.writelines(lines)
    includeString = '#include "VXLIB_meanStdDev_idat.h"'
    idatFile.writelines(includeString)
    idatFile.write("\n")
    idatFile.write("/*This file is autogenerated*/\n")
    idatFile.write("/*Please do not edit*/\n")
    idatFile.write("\n\n")
    # generate ifdefs for header files
    for testCase in testCases:
        if testCase["testType"] == "STATIC":
            VXLIB_utils.gen_ifdefs(idatFile, testCase)

    # generate parameters for test cases
    idatFile.write("static VXLIB_meanStdDev_testParams_t testParams[] =  ")
    idatFile.write("{\n")

    for testCase in testCases:
        gen_testCaseParams(idatFile, testCase)

    idatFile.write("};\n\n")
    fIn = open("test_param_function.txt", "r")
    lines = fIn.readlines()

    idatFile.writelines(lines)


def process_test_case(testCase):
    """Convert str to int for necessary parameters

    Args:
        Param1(:obj: dict) : Dictionary object with test case parameters

    Returns:
        Obj: Dictionary object with necessary parameters converted to int

    """

    # print(testCase.keys())
    testCase["testId"] = int(testCase["ID"])
    testCase["width"] = int(testCase["width"])
    testCase["height"] = int(testCase["height"])
    testCase["strideIn"] = int(testCase["strideIn"])
    testCase["pixelsProcessed"] = int(testCase["pixelsProcessed"])
    testCase["currentSum"] = int(testCase["currentSum"])
    testCase["currentSqSum"] = int(testCase["currentSqSum"])
    return testCase


def gen_test_case_header_file(testCase):
    """Generate test case and write out input, filter, and output tensor
    into a header file usable in C/C++ code.

    Args:
        param1 (:obj: dict): A dict object with all the test case
        parameters

    Returns:
        [int int]: Minimum and maximum value in output feature tensor

    """

    ##############################
    # Get test parameter of case #
    ##############################

    testId = testCase["ID"]
    width = testCase["width"]
    height = testCase["height"]
    dTypeIn = testCase["dTypeIn"]
    dTypeOut = testCase["dTypeOut"]
    pixelsProcessedIn = testCase["pixelsProcessed"]
    currentSumIn = testCase["currentSum"]
    currentSqSumIn = testCase["currentSqSum"]
    
    [minValIn, maxValIn] = VXLIB_utils.resolveDTypeLimits(dTypeIn)
    # [minValOut, maxValOut] = VXLIB_utils.resolveDTypeLimits(dTypeOut)

    in0 = np.random.uniform(low=minValIn, high=maxValIn, size=(height, width)).astype(np.int64)
    
    # out0 = np.mean(in0).astype(VXLIB_utils.resolveNumpyDType(dTypeOut))
    # out1 = np.std(in0).astype(VXLIB_utils.resolveNumpyDType(dTypeOut))

    N = width * height
    pixelsProcessedOut = pixelsProcessedIn + N
    currentSumOut = currentSumIn + np.sum(in0)
    currentSqSumOut = currentSqSumIn + np.sum(in0 * in0)

    currentSumOut_f = np.float64(currentSumOut)
    currentSqSumOut_f = np.float64(currentSqSumOut)

    # calculate mean
    out0 = np.float32(currentSumOut_f / pixelsProcessedOut)

    # calculate standard deviation
    variance = np.float64((currentSqSumOut_f - (currentSumOut_f * currentSumOut_f) / pixelsProcessedOut) / pixelsProcessedOut)
    
    out1 = np.float32(np.sqrt(variance))

    headerFileName = "staticRefCase" + str(testId) + ".h"
    file_io.write_header_file(in0, pixelsProcessedIn, currentSumIn, currentSqSumIn, out0, out1, pixelsProcessedOut, currentSumOut, currentSqSumOut, testCase, headerFileName)

    return [out0, out1]


# CSV file with paratmeters for all test cases
testCasesCsvFile = "test_cases_list.csv"
testIdList = []  # list of test IDs that we want to generate test data
testIdList += range(1, 27)
